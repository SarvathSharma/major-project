// global variables //
let backgroundImage, pacman;

function preload() {
  backgroundImage = loadImage("images/pacman-grid.png");
}

function setup() {
  createCanvas(windowWidth, windowHeight);
}

function draw() {
  makeGrid();
}

function makeGrid() {
  for (let x = 0; x < 27; x++) {
    for (let y = 0; y < 21; y++) {
      if (startingGrid[y][x] === 1) {
        fill("blue");
      }
      if (startingGrid[y][x] === 0) {
        fill(0);
      }
      rect(x * 25, y * 25, 25, 25);
    }
  }
}

class Pacman {
  constructor() {
    this.x = 0;
    this.y = 0;
    this.speed = 1;
    this.direction = 1;
    this.moveUp = false;
    this.moveLeft = false;
    this.moveRight = false;
    this.moveDown = false;
    isMousePressed = false;
  }

  keyPressed() {
    if (keyCode === 38) {
      this.moveUp = true;
    }
    if (keyCode === 37) {
      this.moveLeft = true;
    }
    if (keyCode === 39) {
      this.moveRight = true;
    }
    if (keyCode === 40) {
      this.moveDown = true;
    }
  }

  keyReleased() {
    if (keyCode === 38) {
      this.moveUp = false;
    }
    if (keyCode === 37) {
      this.moveLeft = false;
    }
    if (keyCode === 39) {
      this.moveRight = false;
    }
    if (keyCode === 40) {
      this.moveDown = false;
    }
  }

}
